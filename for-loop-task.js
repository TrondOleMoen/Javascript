// Part 1: Counting up
for (let i = 1; i <= 10; i++) {
  console.log("Iteration:", i);
}

// Part 2: Countdown
console.log("");
console.log("Counting down...");

for (let i = 10; i >= 1; i--) {
  console.log("Countdown:", i);
}

